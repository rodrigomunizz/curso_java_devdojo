package estudo.introducao.operadores;

public class OperadorIncremento {
    public static void main (String[] args ){
        int a = 1;
        int b = 1;
        int c = 2;

        System.out.println(++a);
        System.out.println(b++);
        System.out.println(c);

        System.out.println(a);
        System.out.println(b);
    }
}
